package com.nssol_sh.util.tools.wechat.qy.model.api;

/**
 * API返回值父类
 *
 * @author he.jiaqi
 *
 */
public class APIResultModel {
	/**
	 * 构造函数
	 * 
	 * @param errorCode
	 *            默认错误代码
	 * @param errMsg
	 *            默认错误信息
	 */
	public APIResultModel(int errorCode, String errMsg) {
		// 错误代码赋值
		this.errcode = errorCode;

		// 错误消息赋值
		this.errmsg = errMsg;
	}

	/**
	 * 构造函数
	 * 
	 */
	public APIResultModel() {
	}

	/**
	 * 错误代码.
	 */
	public int errcode;

	/**
	 * 错误信息.
	 */
	public String errmsg;
}
