package com.nssol_sh.entity.wx.view.ysz.yexp.exp0401;

import java.math.BigDecimal;

/**
 * 出差报销-补贴
 * 
 * @author S1mple
 *
 */
public class Exp0401AllowanceModel {

	/**
	 * 序号.
	 */
	public BigDecimal sortKey;

	/**
	 * 补贴项目名.（参照IM，使用message多语言，已弃用）
	 */
	public String itemCd;

	/**
	 * 小计.
	 */
	public BigDecimal subtotal;

	/**
	 * 币种代码.
	 */
	public String currencyCd;

	/**
	 * 币种名.
	 */
	public String currencyName;

	/**
	 * 参考汇率.
	 */
	public BigDecimal exchangeRate;

	/**
	 * 小计（元）.
	 */
	public BigDecimal subtotalLocalCurrency;

	/**
	 * @return the sortKey
	 */
	public BigDecimal getSortKey() {
		return sortKey;
	}

	/**
	 * @return the itemCd
	 */
	public String getItemCd() {
		return itemCd;
	}

	/**
	 * @return the subtotal
	 */
	public BigDecimal getSubtotal() {
		return subtotal;
	}

	/**
	 * @return the currencyCd
	 */
	public String getCurrencyCd() {
		return currencyCd;
	}

	/**
	 * @return the currencyName
	 */
	public String getCurrencyName() {
		return currencyName;
	}

	/**
	 * @return the exchangeRate
	 */
	public BigDecimal getExchangeRate() {
		return exchangeRate;
	}

	/**
	 * @return the subtotalLocalCurrency
	 */
	public BigDecimal getSubtotalLocalCurrency() {
		return subtotalLocalCurrency;
	}

}
